#include "fractol.h"

void			zoom(int av, t_mlx *mlx)
{
	if (av == 1 && mlx->zoom - 0.5 > 0)
		mlx->zoom -= 0.5;
	else if (av == 0)
		mlx->zoom += 0.5;
	generate_new_image(mlx);
}
